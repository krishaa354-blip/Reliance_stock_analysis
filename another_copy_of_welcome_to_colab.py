# A simple way to plot candlesticks using matplotlib without mplfinance library
def plot_candlestick(df_data, ax):
    for index, row in df_data.iterrows():
        color = 'green' if row['Close'] > row['Open'] else 'red'
        # Plot the wick
        ax.plot([row['Date'], row['Date']], [row['Low'], row['High']], color='black')
        # Plot the body
        ax.plot([row['Date'], row['Date']], [row['Open'], row['Close']], color=color, linewidth=5)

