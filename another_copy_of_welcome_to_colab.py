# --- Insight 10: Maximum Drawdown Analysis --- (Drawdown Chart)
print("Chart Name: Maximum Drawdown Analysis")
print("Chart Type: Drawdown Chart (using filled area)")
print("Variables Used: X-axis: Date, Y-axis: Drawdown (%)")
print("Color Logic: Red shades to represent drawdowns, darker for deeper ones. Horizontal line at 0% for peak reference.")

# 1. (Already done: Date column converted to datetime and data sorted in previous cells)
# df['Date'] = pd.to_datetime(df['Date'])
# df = df.sort_values(by='Date').reset_index(drop=True)

# 2. Calculate cumulative maximum of Close price
df['Cumulative_Max_Close'] = df['Close'].cummax()

# 3. Compute Drawdown using formula
df['Drawdown_Percentage'] = ((df['Close'] - df['Cumulative_Max_Close']) / df['Cumulative_Max_Close']) * 100

# 4. Plot drawdown over time
plt.figure(figsize=(14, 7))

# Plot the drawdown line itself (optional, can be transparent or light color)
plt.plot(df['Date'], df['Drawdown_Percentage'], color='lightgrey', linestyle='-', linewidth=0.8, alpha=0.7, label='Drawdown Level')

# Use fill_between for shaded region to highlight drawdowns (negative values)
# The 'where' condition ensures only the negative (drawdown) areas are filled.
# Deeper red for deeper drawdowns is implicitly shown by the depth of the red area.
plt.fill_between(df['Date'], df['Drawdown_Percentage'], 0,
                 where=(df['Drawdown_Percentage'] < 0),
                 color='red', alpha=0.6, label='Drawdown Area')

# Add a horizontal line at 0% (peak level reference)
plt.axhline(0, color='black', linestyle='--', linewidth=1, label='Peak Level (0% Drawdown)')

# Labels
plt.title('Reliance Industries - Maximum Drawdown Analysis', fontsize=16)
plt.xlabel('Date', fontsize=12)
plt.ylabel('Drawdown (%)', fontsize=12)

# Add grid for readability
plt.grid(True, linestyle='--', alpha=0.7)
plt.legend()
plt.tight_layout()
plt.show()

print("Insight / Interpretation: This drawdown chart visually represents the percentage decline from the stock's historical peak values. It is a critical risk metric, showing how much an investment has fallen from its highest point. Frequent and deep drawdowns (indicated by larger red shaded areas) suggest higher volatility and risk in the stock's behavior, while prolonged periods near the 0% line indicate sustained high performance or recovery.")
